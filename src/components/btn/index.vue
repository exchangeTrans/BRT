<template>
    <view class="btn"  :style="btnStyle" id="btn" @tap="btnClick">
        {{btnText}}
    </view>

</template>

<script>
    export default {
        name: "btn",
        props:{
            btnText:{
                type:String,
                default:'чбошод'
            },
            backgroundColor:{type:String, default:''},
            borderRadius:{type:String, default:''},
            fontColor:{type:String, default:''},
            Opacity:{type:String, default:''},
            width:{type:String, default:''},

        },
        computed: {
            btnStyle () {
                let {
                    backgroundColor,
                    borderRadius,
                    fontColor,
                    Opacity,
                    width,
                } = this.$props
                return {
                    backgroundColor: backgroundColor,
                    borderRadius,
                    color: fontColor,
                    opacity: Opacity,
                    width,
                }
            }
        },
        data(){
            return {

            }
        },
        methods:{
            btnClick(){
                this.$emit('btnClick')
            }
        }
    }
</script>

